import Foundation

extension Character {
    var asString: String {
        return String(self)
    }
}

extension Int {
    var asString: String {
        return String(self)
    }
}

extension Double {
    var asString: String {
        return String(self)
    }
}

extension Float {
    var asString: String {
        return String(self)
    }
}

extension Bool {
    var asString: String {
        return String(self)
    }
}
